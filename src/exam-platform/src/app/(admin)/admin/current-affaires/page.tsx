import { useQuery, useMutation, useQueryClient } from "@tanstack/react-query";
import { customFetch } from "./custom-fetch";
import type { CurrentAffair } from "./generated/api.schemas";

// Types
export interface CurrentAffairsList {
  data: CurrentAffair[];
  total: number;
  page: number;
  totalPages: number;
}

// API functions
const fetchCurrentAffairs = async (params?: { page?: number; limit?: number; search?: string; category?: string }): Promise<CurrentAffairsList> => {
  const queryParams = new URLSearchParams();
  if (params?.page) queryParams.append("page", params.page.toString());
  if (params?.limit) queryParams.append("limit", params.limit.toString());
  if (params?.search) queryParams.append("search", params.search);
  if (params?.category) queryParams.append("category", params.category);
  const qStr = queryParams.toString();
  return customFetch<CurrentAffairsList>(`/api/admin/current-affairs${qStr ? `?${qStr}` : ""}`, { method: "GET" });
};

const fetchCurrentAffair = async (id: number): Promise<CurrentAffair> => {
  return customFetch<CurrentAffair>(`/api/admin/current-affairs/${id}`, { method: "GET" });
};

const createCurrentAffair = async (data: Omit<CurrentAffair, "id">): Promise<CurrentAffair> => {
  return customFetch<CurrentAffair>("/api/admin/current-affairs", {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(data),
  });
};

const updateCurrentAffair = async (id: number, data: Partial<CurrentAffair>): Promise<CurrentAffair> => {
  return customFetch<CurrentAffair>(`/api/admin/current-affairs/${id}`, {
    method: "PATCH",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(data),
  });
};

const deleteCurrentAffair = async (id: number): Promise<void> => {
  return customFetch(`/api/admin/current-affairs/${id}`, { method: "DELETE" });
};

// Hooks
export const useCurrentAffairs = (params?: { page?: number; limit?: number; search?: string; category?: string }) => {
  return useQuery<CurrentAffairsList, Error>({
    queryKey: ["admin", "current-affairs", params],
    queryFn: () => fetchCurrentAffairs(params),
    staleTime: 60000,
  });
};

export const useCurrentAffair = (id: number) => {
  return useQuery<CurrentAffair, Error>({
    queryKey: ["admin", "current-affairs", id],
    queryFn: () => fetchCurrentAffair(id),
    enabled: !!id,
  });
};

export const useCreateCurrentAffair = () => {
  const qc = useQueryClient();
  return useMutation<CurrentAffair, Error, Omit<CurrentAffair, "id">>({
    mutationFn: createCurrentAffair,
    onSuccess: () => qc.invalidateQueries({ queryKey: ["admin", "current-affairs"] }),
  });
};

export const useUpdateCurrentAffair = (id: number) => {
  const qc = useQueryClient();
  return useMutation<CurrentAffair, Error, Partial<CurrentAffair>>({
    mutationFn: (data) => updateCurrentAffair(id, data),
    onSuccess: () => {
      qc.invalidateQueries({ queryKey: ["admin", "current-affairs"] });
      qc.invalidateQueries({ queryKey: ["admin", "current-affairs", id] });
    },
  });
};

export const useDeleteCurrentAffair = () => {
  const qc = useQueryClient();
  return useMutation<void, Error, number>({
    mutationFn: deleteCurrentAffair,
    onSuccess: () => qc.invalidateQueries({ queryKey: ["admin", "current-affairs"] }),
  });
};